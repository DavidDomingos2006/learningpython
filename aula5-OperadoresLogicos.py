#os operadores normais agem normalmente + - / *
#Mas existem as excessões, segue abaixo
pontencia = 2 ** 10
print("2 elevado a 10 =",pontencia)

divisao_que_retorna_inteiro = 10 // 3
print("Usando // na divisão, retornamos apenas o valor inteiro da divisão = ",divisao_que_retorna_inteiro)

modulo = 10 % 3 # tras como resultado o resto da divisão
print("Usando '%' como operador o retorno é o resto da divisão",modulo)
#modulo é a ferramenta para saber se um resultado é par ou impar 11 % 2 = 1 (impar)